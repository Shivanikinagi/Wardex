/**
 * generate-zk-proof.js
 *
 * Generates a ZK compliance proof and submits it to the deployed Verifier.sol.
 *
 * Two modes:
 *   1. REAL  — if `nargo` is on $PATH, runs `nargo prove` to generate an
 *              actual UltraPlonk proof from circuits/src/main.nr
 *   2. DEMO  — otherwise, generates a structured proof commitment that the
 *              Verifier accepts (hackathon-compatible simulation)
 *
 * Usage:
 *   node scripts/generate-zk-proof.js                    # trading-agent default
 *   node scripts/generate-zk-proof.js data               # data-agent
 *   node scripts/generate-zk-proof.js trading whitelist  # specific proof type
 */

require('dotenv').config()
const { ethers } = require('ethers')
const { execSync, spawnSync } = require('child_process')
const fs = require('fs')
const path = require('path')

// ─── Config ──────────────────────────────────────────────────────────────────

const VERIFIER_ADDRESS = process.env.VERIFIER_CONTRACT || '0x7D5d1222e12D1D26F512a9626B68ce2394C7e034'
const AGENTS = {
    trading: {
        address: process.env.TRADING_AGENT_ADDRESS || '0x4B02abfffd2f4a0De9bdf0Ea3Eb73271014EFb60',
        ens: 'trading-agent.dark26.eth',
        spendingLimit: ethers.parseEther('0.01'),
        dailyLimit: ethers.parseEther('0.1'),
    },
    data: {
        address: process.env.DATA_AGENT_ADDRESS || '0xA28FA8e3391f4454F8E555F5A1Ef5ECC7486dF4F',
        ens: 'data-agent.dark26.eth',
        spendingLimit: ethers.parseEther('0.005'),
        dailyLimit: ethers.parseEther('0.05'),
    },
}

const PROOF_TYPES = ['spending_limit', 'whitelist', 'sanctions', 'attestation']

const VERIFIER_ABI = [
    'function submitAndVerifyProof(address agent, bytes calldata proofData, string calldata proofType, uint256[2] calldata publicInputs) external returns (bool)',
    'function getComplianceStatus(address agent) external view returns (tuple(bool isCompliant, uint256 totalProofs, uint256 verifiedProofs, uint256 failedProofs, uint256 lastProofTime, string lastProofType))',
]

// ─── Helpers ─────────────────────────────────────────────────────────────────

function hasNargo() {
    try {
        const result = spawnSync('nargo', ['--version'], { timeout: 5000 })
        return result.status === 0
    } catch {
        return false
    }
}

function buildProverToml(agentKey, proofType, attestationHash) {
    // Private inputs
    const txAmount = '100'       // 100 wei (well within limit)
    const dailyTotal = '50'
    const recipientHash = '12345678901234567890'
    const agentCapHash = '98765432109876543210'
    const actionHash   = '11111111111111111111'

    // Public inputs
    const maxPerTx  = '1000'
    const maxPerDay = '10000'
    const whitelistRoot   = '22222222222222222222'
    const sanctionsHash   = '33333333333333333333'
    const expectedAttest  = attestationHash
    const capabilityRoot  = '44444444444444444444'

    return `# DarkAgent Compliance Prover Input
# Generated by scripts/generate-zk-proof.js
# Agent: ${AGENTS[agentKey].ens}
# Proof type: ${proofType}

transaction_amount = "${txAmount}"
daily_total = "${dailyTotal}"
recipient_hash = "${recipientHash}"
attestation_hash = "${expectedAttest}"
agent_capability_hash = "${agentCapHash}"
action_hash = "${actionHash}"
max_per_transaction = "${maxPerTx}"
max_per_day = "${maxPerDay}"
whitelist_merkle_root = "${whitelistRoot}"
sanctions_list_hash = "${sanctionsHash}"
expected_attestation = "${expectedAttest}"
capability_root = "${capabilityRoot}"
`
}

function generateDemoProof(agentAddress, proofType, attestationHash) {
    // Structured deterministic commitment — not a real ZK proof, but carries
    // all the right semantic fields for the hackathon verifier.
    const fields = {
        version: '1',
        circuit: 'darkagent_compliance',
        proofType,
        agent: agentAddress,
        attestationHash,
        publicInputs: {
            max_per_transaction: '1000',
            max_per_day: '10000',
            whitelist_merkle_root: '22222222222222222222',
            sanctions_list_hash: '33333333333333333333',
        },
        timestamp: Date.now().toString(),
        // Simulated UltraPlonk proof bytes
        proofCommitment: ethers.keccak256(
            ethers.toUtf8Bytes(`noir-proof-${proofType}-${agentAddress}-${Date.now()}`)
        ),
    }
    return ethers.toUtf8Bytes(JSON.stringify(fields))
}

async function runNargoProof(circuitDir, proverToml) {
    // Write Prover.toml
    const proverPath = path.join(circuitDir, 'Prover.toml')
    fs.writeFileSync(proverPath, proverToml)

    console.log('  Running: nargo prove ...')
    const result = spawnSync('nargo', ['prove'], {
        cwd: circuitDir,
        timeout: 120_000,
        stdio: ['ignore', 'pipe', 'pipe'],
    })

    if (result.status !== 0) {
        throw new Error(`nargo prove failed:\n${result.stderr?.toString()}`)
    }

    // Read the generated proof
    const proofPath = path.join(circuitDir, 'proofs', 'darkagent_compliance.proof')
    if (!fs.existsSync(proofPath)) {
        throw new Error('Proof file not found after nargo prove')
    }
    const proofHex = fs.readFileSync(proofPath, 'utf8').trim()
    return ethers.getBytes('0x' + proofHex)
}

// ─── Main ────────────────────────────────────────────────────────────────────

async function main() {
    const agentKey  = (process.argv[2] || 'trading').toLowerCase()
    const proofType = (process.argv[3] || 'spending_limit').toLowerCase()

    if (!AGENTS[agentKey]) {
        console.error(`Unknown agent: ${agentKey}. Use "trading" or "data"`)
        process.exit(1)
    }
    if (!PROOF_TYPES.includes(proofType)) {
        console.error(`Unknown proof type: ${proofType}. Use: ${PROOF_TYPES.join(' | ')}`)
        process.exit(1)
    }

    const agent = AGENTS[agentKey]

    console.log('═══════════════════════════════════════════════════════════')
    console.log('     DarkAgent ZK Compliance Proof Generator')
    console.log('═══════════════════════════════════════════════════════════\n')
    console.log('Agent:     ', agent.ens)
    console.log('Address:   ', agent.address)
    console.log('Proof type:', proofType)

    // Connect to Base Sepolia
    const provider = new ethers.JsonRpcProvider(process.env.BASE_SEPOLIA_RPC || 'https://sepolia.base.org')
    const signer   = new ethers.Wallet(process.env.PRIVATE_KEY, provider)
    const verifier = new ethers.Contract(VERIFIER_ADDRESS, VERIFIER_ABI, signer)

    console.log('Submitter: ', signer.address)
    const balance = await provider.getBalance(signer.address)
    console.log('Balance:   ', ethers.formatEther(balance), 'ETH (Base Sepolia)\n')

    // Attestation hash (simulate MRENCLAVE from TEE)
    const attestationHash = ethers.keccak256(
        ethers.toUtf8Bytes('darkagent-v1.0.0-MRENCLAVE-BASE')
    )
    // Remove 0x prefix for numeric use in Prover.toml
    const attestNum = BigInt(attestationHash).toString()

    // Public inputs for Verifier.sol
    // spending_limit: [amount_spent, spending_limit]
    // whitelist:     [whitelist_root_hash, 1]
    // sanctions:     [0, 1]  (0 = no sanction match)
    // attestation:   [attestation_hash_as_uint, 1]
    let publicInputs
    if (proofType === 'spending_limit') {
        publicInputs = [ethers.parseEther('0.002'), agent.spendingLimit]
    } else if (proofType === 'whitelist') {
        publicInputs = [BigInt('0x22222222222222222222222222222222'), 1n]
    } else if (proofType === 'sanctions') {
        publicInputs = [0n, 1n]
    } else {
        publicInputs = [BigInt(attestationHash), 1n]
    }

    // ── Generate proof ──────────────────────────────────────────────────────
    let proofBytes
    const circuitDir = path.join(__dirname, '..', 'circuits')

    if (hasNargo()) {
        console.log('✅ nargo found — generating REAL UltraPlonk proof\n')
        const proverToml = buildProverToml(agentKey, proofType, attestNum)
        proofBytes = await runNargoProof(circuitDir, proverToml)
        console.log('  Proof size:', proofBytes.length, 'bytes')
    } else {
        console.log('⚠️  nargo not installed — generating structured demo proof')
        console.log('   (Install nargo on Linux/macOS for real ZK proofs)\n')
        proofBytes = generateDemoProof(agent.address, proofType, attestationHash)
    }

    // ── Submit to Verifier.sol ──────────────────────────────────────────────
    console.log('Submitting proof to Verifier.sol on Base Sepolia...')
    const tx = await verifier.submitAndVerifyProof(
        agent.address,
        proofBytes,
        proofType,
        publicInputs
    )
    console.log('TX sent:  ', tx.hash)
    const receipt = await tx.wait()
    console.log('Confirmed: block', receipt.blockNumber)

    // ── Read result ─────────────────────────────────────────────────────────
    const status = await verifier.getComplianceStatus(agent.address)
    const verified = status.verifiedProofs > 0n

    console.log('\n──────────────────────────────────────────────')
    console.log(verified ? '✅ COMPLIANT' : '❌ NON-COMPLIANT')
    console.log('  Total proofs:    ', status.totalProofs.toString())
    console.log('  Verified proofs: ', status.verifiedProofs.toString())
    console.log('  Failed proofs:   ', status.failedProofs.toString())
    console.log('  Last proof type: ', status.lastProofType)
    console.log('──────────────────────────────────────────────')
    console.log('\nBaseScan TX:', `https://sepolia.basescan.org/tx/${receipt.hash}`)
    console.log('BaseScan Contract:', `https://sepolia.basescan.org/address/${VERIFIER_ADDRESS}`)
}

main().catch(err => {
    console.error('❌ Error:', err.message)
    process.exit(1)
})
